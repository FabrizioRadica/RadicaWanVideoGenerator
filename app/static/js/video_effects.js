/* Color & Look — live canvas preview + settings persistence.
   The canvas pipeline approximates the ffmpeg chain used on final export
   (see app/services/video_effects_service.py). Preview only — the video is
   never regenerated by slider changes.
   Radica - WanVideoGenerator — Concept & Design: Fabrizio Radica — Project by RadicaDesign */

(function () {
  "use strict";

  var fx = null;             // current settings (mirror of server state)
  var baseFrame = null;      // ImageData of the untouched preview frame
  var canvas, ctx;
  var patchTimer = null;
  var renderPending = false;

  function el(id) { return document.getElementById(id); }
  function api(path, opts) { return WVG.api("/api/projects/" + WVG.project.id + path, opts); }

  /* ---------- settings helpers ---------- */

  function getPath(obj, path) {
    return path.split(".").reduce(function (o, k) { return o ? o[k] : undefined; }, obj);
  }
  function setPath(obj, path, value) {
    var keys = path.split(".");
    var target = obj;
    for (var i = 0; i < keys.length - 1; i++) target = target[keys[i]];
    target[keys[keys.length - 1]] = value;
  }
  function nested(path, value) {
    var keys = path.split(".");
    if (keys.length === 1) { var o = {}; o[path] = value; return o; }
    var inner = {}; inner[keys[1]] = value;
    var outer = {}; outer[keys[0]] = inner;
    return outer;
  }

  function persist(changes) {
    clearTimeout(patchTimer);
    patchTimer = setTimeout(function () {
      api("/video-effects", { method: "PATCH", body: changes }).then(function (updated) {
        fx = updated;
      }).catch(function (e) { WVG.toast("Could not save effect settings", "error", e.message); });
    }, 350);
  }

  /* ---------- preview frame source ---------- */

  function previewSource() {
    var vids = (WVG.project.generated_videos || []).filter(function (v) { return v.preview; });
    if (vids.length) {
      return { url: "/media/projects/" + WVG.project.id + "/previews/" + vids[vids.length - 1].preview,
               label: "Frame from " + vids[vids.length - 1].filename };
    }
    if (WVG.project.source_image) {
      return { url: "/media/projects/" + WVG.project.id + "/source/" + WVG.project.source_image,
               label: "Source image (no generated frame yet)" };
    }
    return null;
  }

  function loadFrame() {
    var src = previewSource();
    var caption = el("vfx-preview-caption");
    if (!src) {
      // Clearly-marked placeholder gradient — never pretends to be a real frame.
      var g = ctx.createLinearGradient(0, 0, canvas.width, canvas.height);
      g.addColorStop(0, "#3b3663"); g.addColorStop(1, "#171426");
      ctx.fillStyle = g;
      ctx.fillRect(0, 0, canvas.width, canvas.height);
      ctx.fillStyle = "#b9a5ff"; ctx.font = "11px sans-serif"; ctx.textAlign = "center";
      ctx.fillText("PLACEHOLDER — generate a video for a real preview frame",
                   canvas.width / 2, canvas.height / 2);
      baseFrame = ctx.getImageData(0, 0, canvas.width, canvas.height);
      render();
      return;
    }
    var img = new Image();
    img.onload = function () {
      var maxW = 640;
      var scale = Math.min(1, maxW / img.width);
      canvas.width = Math.round(img.width * scale);
      canvas.height = Math.round(img.height * scale);
      ctx.drawImage(img, 0, 0, canvas.width, canvas.height);
      baseFrame = ctx.getImageData(0, 0, canvas.width, canvas.height);
      caption.textContent = "Preview only — applied to the final export during post-processing (" + src.label + ")";
      render();
    };
    img.src = src.url + "?t=" + Date.now();
  }

  /* ---------- canvas effect pipeline ---------- */

  function render() {
    if (!baseFrame || renderPending) return;
    renderPending = true;
    requestAnimationFrame(function () {
      renderPending = false;
      // Shared Color & Look pipeline (patchReuseColoAudio §5) — the same code
      // path the sequence preview uses. Local fallback keeps Single Clip working
      // if the shared module failed to load.
      if (window.WVGColorLook && WVGColorLook.pipeline) {
        WVGColorLook.pipeline.renderInto(ctx, canvas, baseFrame, fx);
        return;
      }
      var w = canvas.width, h = canvas.height;
      var out = new ImageData(new Uint8ClampedArray(baseFrame.data), w, h);
      if (fx.enabled) {
        pixelPass(out.data, w, h);
        if (fx.sharpness.enabled && fx.sharpness.amount > 0) sharpen(out, w, h, fx.sharpness.amount);
      }
      ctx.putImageData(out, 0, 0);
      if (fx.enabled) {
        if (fx.vhs_effect.enabled && fx.vhs_effect.intensity > 0) vhsOverlay(w, h);
        if (fx.vignette.enabled && fx.vignette.intensity > 0) vignetteOverlay(w, h);
        if (fx.film_grain.enabled && fx.film_grain.intensity > 0) grainOverlay(w, h);
      }
      canvas.style.opacity = fx.enabled ? "1" : "0.85";
    });
  }

  function pixelPass(d, w, h) {
    var sat = fx.saturation, con = fx.contrast, bri = fx.brightness / 100 * 128;
    var gam = 1 / fx.gamma, temp = fx.temperature, hue = fx.hue * Math.PI / 180;
    var sh = fx.shadows / 100, hi = fx.highlights / 100;
    var cosH = Math.cos(hue), sinH = Math.sin(hue);
    var caShift = 0;
    if (fx.vhs_effect.enabled && fx.vhs_effect.intensity > 0) {
      caShift = Math.round(fx.vhs_effect.chromatic_aberration * fx.vhs_effect.intensity * 8);
    }
    var gammaLUT = new Uint8ClampedArray(256);
    for (var i = 0; i < 256; i++) gammaLUT[i] = 255 * Math.pow(i / 255, gam);
    var src = caShift ? new Uint8ClampedArray(d) : null;

    for (var p = 0; p < d.length; p += 4) {
      var r = d[p], g = d[p + 1], b = d[p + 2];
      if (caShift) {
        var x = (p / 4) % w;
        var xr = Math.min(w - 1, x + caShift), xb = Math.max(0, x - caShift);
        var row = p - x * 4;
        r = src[row + xr * 4];
        b = src[row + xb * 4 + 2];
      }
      // gamma -> brightness/contrast -> temperature -> hue rotate -> saturation -> tone curve
      r = gammaLUT[r]; g = gammaLUT[g]; b = gammaLUT[b];
      r = (r - 128) * con + 128 + bri; g = (g - 128) * con + 128 + bri; b = (b - 128) * con + 128 + bri;
      if (temp) { r += temp * 0.3; b -= temp * 0.3; }
      if (hue) {
        // YIQ hue rotation
        var Y = 0.299 * r + 0.587 * g + 0.114 * b;
        var I = 0.596 * r - 0.274 * g - 0.322 * b;
        var Q = 0.211 * r - 0.523 * g + 0.312 * b;
        var I2 = I * cosH - Q * sinH, Q2 = I * sinH + Q * cosH;
        r = Y + 0.956 * I2 + 0.621 * Q2; g = Y - 0.272 * I2 - 0.647 * Q2; b = Y - 1.106 * I2 + 1.703 * Q2;
      }
      var lum = 0.299 * r + 0.587 * g + 0.114 * b;
      if (sat !== 1) { r = lum + (r - lum) * sat; g = lum + (g - lum) * sat; b = lum + (b - lum) * sat; }
      if (sh || hi) {
        var ln = Math.min(Math.max(lum / 255, 0), 1);
        var shadowW = 1 - ln, highlightW = ln;
        var lift = sh * 46 * shadowW * shadowW + hi * 46 * highlightW * highlightW;
        r += lift; g += lift; b += lift;
      }
      d[p] = r; d[p + 1] = g; d[p + 2] = b;
    }
  }

  function sharpen(image, w, h, amount) {
    var s = image.data, copy = new Uint8ClampedArray(s);
    var a = amount * 0.55;
    for (var y = 1; y < h - 1; y++) {
      for (var x = 1; x < w - 1; x++) {
        var p = (y * w + x) * 4;
        for (var c = 0; c < 3; c++) {
          var v = copy[p + c] * (1 + 4 * a)
                - a * (copy[p - 4 + c] + copy[p + 4 + c] + copy[p - w * 4 + c] + copy[p + w * 4 + c]);
          s[p + c] = v;
        }
      }
    }
  }

  function vignetteOverlay(w, h) {
    var v = fx.vignette;
    var cx = w / 2, cy = h / 2;
    var maxR = Math.sqrt(cx * cx + cy * cy);
    var inner = maxR * (0.25 + 0.65 * v.radius);
    var outer = inner + maxR * (0.15 + 0.85 * v.softness);
    var grad = ctx.createRadialGradient(cx, cy, inner, cx, cy, outer);
    grad.addColorStop(0, "rgba(0,0,0,0)");
    grad.addColorStop(1, "rgba(0,0,0," + Math.min(v.intensity, 1) + ")");
    ctx.fillStyle = grad;
    ctx.fillRect(0, 0, w, h);
  }

  function grainOverlay(w, h) {
    var g = fx.film_grain;
    var size = Math.max(1, Math.round(1 + g.grain_size * 3));
    var gw = Math.ceil(w / size), gh = Math.ceil(h / size);
    var noise = ctx.createImageData(gw, gh);
    for (var p = 0; p < noise.data.length; p += 4) {
      var n = 128 + (Math.random() - 0.5) * 255;
      noise.data[p] = noise.data[p + 1] = noise.data[p + 2] = n;
      noise.data[p + 3] = 255;
    }
    var tmp = document.createElement("canvas");
    tmp.width = gw; tmp.height = gh;
    tmp.getContext("2d").putImageData(noise, 0, 0);
    ctx.save();
    ctx.globalAlpha = g.intensity * 0.55;
    ctx.globalCompositeOperation = "overlay";
    ctx.imageSmoothingEnabled = false;
    ctx.drawImage(tmp, 0, 0, gw, gh, 0, 0, w, h);
    ctx.restore();
  }

  function vhsOverlay(w, h) {
    var v = fx.vhs_effect, m = v.intensity;
    // horizontal jitter: shift a few random slices
    if (v.jitter > 0 || v.tracking_distortion > 0) {
      var slices = 3 + Math.round(v.tracking_distortion * 5);
      for (var i = 0; i < slices; i++) {
        var sy = Math.floor(Math.random() * h);
        var sh2 = 2 + Math.floor(Math.random() * 6);
        var dx = Math.round((Math.random() - 0.5) * 2 * (v.jitter * m * 14 + v.tracking_distortion * m * 6));
        if (dx) ctx.drawImage(canvas, 0, sy, w, sh2, dx, sy, w, sh2);
      }
    }
    // noise + tape damage speckles
    if (v.noise > 0 || v.tape_damage > 0) {
      ctx.save();
      ctx.globalAlpha = Math.min((v.noise * 0.5 + v.tape_damage * 0.4) * m, 1);
      for (var n = 0; n < 220 * m; n++) {
        var nx = Math.random() * w, ny = Math.random() * h;
        ctx.fillStyle = Math.random() > 0.5 ? "rgba(255,255,255,0.5)" : "rgba(0,0,0,0.5)";
        ctx.fillRect(nx, ny, 1 + Math.random() * (1 + v.tape_damage * 8), 1);
      }
      ctx.restore();
    }
    // scanlines
    if (v.scanlines > 0) {
      ctx.save();
      ctx.fillStyle = "rgba(0,0,0," + Math.min(v.scanlines * m, 1) * 0.4 + ")";
      for (var y = 0; y < h; y += 3) ctx.fillRect(0, y, w, 1);
      ctx.restore();
    }
    // color bleeding: slight blurred copy on top
    if (v.color_bleeding > 0) {
      ctx.save();
      ctx.globalAlpha = Math.min(v.color_bleeding * m * 0.9, 0.7);
      ctx.filter = "blur(" + (v.color_bleeding * m * 4).toFixed(1) + "px) saturate(1.4)";
      ctx.drawImage(canvas, 0, 0);
      ctx.restore();
      ctx.filter = "none";
    }
  }

  /* ---------- UI wiring ---------- */

  function fmt(path, value) {
    if (path === "hue") return Math.round(value) + "°";
    if (["temperature", "shadows", "highlights", "brightness"].indexOf(path) !== -1) return String(Math.round(value));
    return Number(value).toFixed(2);
  }

  function bindSliders() {
    document.querySelectorAll("#vfx-controls .vfx-slider").forEach(function (row) {
      var path = row.dataset.path;
      var input = row.querySelector("input");
      var out = row.querySelector("b");
      input.value = getPath(fx, path);
      out.textContent = fmt(path, input.value);
      input.addEventListener("input", function () {
        var v = parseFloat(input.value);
        out.textContent = fmt(path, v);
        setPath(fx, path, v);
        persist(nested(path, v));
        render();
      });
    });
  }

  function bindToggle(id, path) {
    var input = el(id);
    input.checked = !!getPath(fx, path);
    input.addEventListener("change", function (e) {
      e.stopPropagation();
      setPath(fx, path, input.checked);
      persist(nested(path, input.checked));
      render();
    });
  }

  function populateTargets() {
    var sel = el("vfx-apply-target");
    var raw = (WVG.project.generated_videos || []).filter(function (v) {
      return !v.has_audio && !v.has_effects;
    });
    sel.innerHTML = raw.length
      ? raw.slice().reverse().map(function (v) {
          return '<option value="' + v.filename.replace(/"/g, "&quot;") + '">' + v.filename + "</option>";
        }).join("")
      : '<option value="">— no raw generated video yet —</option>';
  }

  async function applyEffects() {
    var target = el("vfx-apply-target").value;
    if (!target) { WVG.toast("Generate a video first", "error"); return; }
    if (!fx.enabled) { WVG.toast("Enable video effects first", "error"); return; }
    var btn = el("btn-apply-vfx");
    btn.disabled = true;
    btn.textContent = "Rendering…";
    try {
      var res = await api("/video-effects/apply", { method: "POST", body: { video_filename: target } });
      WVG.toast("Effects applied: " + res.entry.filename, "success");
      if (window.WVGGen) WVGGen.showOutput(res.entry.filename);
      setTimeout(function () { window.location = "/?project=" + WVG.project.id; }, 1200);
    } catch (e) {
      WVG.toast("Video effects failed", "error", e.message);
    } finally {
      btn.disabled = false;
      btn.innerHTML = "✨ Apply Effects";
    }
  }

  document.addEventListener("DOMContentLoaded", function () {
    if (!el("vfx-panel") || !WVG.project) return;
    canvas = el("vfx-preview");
    ctx = canvas.getContext("2d", { willReadFrequently: true });
    fx = WVG.project.video_effects || { enabled: false };

    el("vfx-enabled").checked = !!fx.enabled;
    el("vfx-enabled").addEventListener("change", function () {
      fx.enabled = this.checked;
      persist({ enabled: fx.enabled });
      render();
    });

    bindSliders();
    bindToggle("vfx-vignette-on", "vignette.enabled");
    bindToggle("vfx-grain-on", "film_grain.enabled");
    bindToggle("vfx-sharp-on", "sharpness.enabled");
    bindToggle("vfx-vhs-on", "vhs_effect.enabled");
    el("vfx-grain-animated").checked = !!fx.film_grain.animated;
    el("vfx-grain-animated").addEventListener("change", function () {
      fx.film_grain.animated = this.checked;
      persist({ film_grain: { animated: this.checked } });
    });

    populateTargets();
    el("btn-apply-vfx").addEventListener("click", applyEffects);
    loadFrame();
  });
})();
